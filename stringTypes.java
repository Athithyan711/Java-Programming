package stringBasic;

public class stringTypes {
	public static void main(String[] args) {
		String st1="athi";
		String st2="athi";
		System.out.println(st1==st2);
		System.out.println(st1.equals(st2));
		System.out.println(st1.equalsIgnoreCase(st2));
		System.out.println(st1.length());
	}

}
